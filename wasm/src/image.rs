use anyhow::Context;
use image::EncodableLayout;

pub fn from_string_to_f64_array(input: Option<String>) -> anyhow::Result<Vec<f64>> {
    let input = input.context("Rust from_string_to_f64_array input string is empty")?;
    let base64_encoded_png = input.replace("data:image/png;base64,", "");
    let decoded_image = base64::decode(base64_encoded_png)?;
    let image = image::load_from_memory_with_format(&decoded_image, image::ImageFormat::Png)?;
    Ok(image
        .to_luma8() // same as mnist dataset
        .as_bytes()
        .iter()
        .map(|value| convert(*value))
        .map(|value| (((value as f64) / 255.0) * 0.99) + 0.01)
    .collect::<Vec<f64>>())
}

/// Need this convert method, because in the mnist dataset
/// 0 means background (white), 255 means foreground (black),
/// while load_from_memory_with_format returns 255 was white
/// and 0 as black
fn convert(input: u8) -> u8 {
    (255 as u8).abs_diff(input)
}

